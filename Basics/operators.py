# Arithmetic Operators 
# Addition = +
# Substrtaction = -
# Multiplication = *
# Division (Quotient) = / 
# Mod (Reminder) = %
# Exponential(Power) = **
# Floor Division = // #14.44 --> 14

x = 5
y = 3 

print(x+y) # Addition
print(x-y) # Substrtaction
print(x*y) # Multiplication
print(x/y) # Division
print(x%y) # Mod (Reminder)
print(x**y) # Exponential(Power)
print(x//y) # Floor Division 

